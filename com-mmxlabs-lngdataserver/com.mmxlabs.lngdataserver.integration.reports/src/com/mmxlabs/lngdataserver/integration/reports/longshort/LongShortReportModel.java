public class LongShortReportModel {
	
	int year;
	int month;
	int longs;
	int shorts;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getLongs() {
		return longs;
	}
	public void setLongs(int longs) {
		this.longs = longs;
	}
	public int getShorts() {
		return shorts;
	}
	public void setShorts(int shorts) {
		this.shorts = shorts;
	}
}
